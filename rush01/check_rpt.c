int	not_rpt(char form[4][4], int pos, char num)
{
	int	i;

	i = 0;
	while (i < pos / 4)
	{
		if (form[i][pos % 4] == num)
			return (0);
		i++;
	}
	i = 0;
	while (i < pos % 4)
	{
		if (form[pos / 4][i] == num)
			return (0);
		i++;
	}
	return (1);
}
